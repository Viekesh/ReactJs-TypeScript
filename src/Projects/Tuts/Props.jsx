// Props :

// Props stand for "properties". They are read only component. It is an object which
// stores the value of attribute of a tag and work similar to tha html attributes.

// It gives a way to pass data from one component to other components.
// It is similar to function arguments. 
// Props are passed to the component in the same way as arguments passed in a function.

// Props are "immutable" so we cannot modify the props from inside the component. Inside
// the component we can add the attribute called props



// State and Props :

// It is possible to combine both state and props.
// You can set the state in the parent component and pass it in a child component using props.